#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int		a;
	char	*b;

	b = (char *)s;
	a = 0;
	while (b[a] != c && b[a] != '\0')
		a++;
	if (b[a] == c)
		return (&b[a]);
	else
		return (NULL);
}
